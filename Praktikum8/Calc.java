    private void expandNumb(Integer newNumb){
        if(notDecimal) {
            currentNumb *= 10;
            currentNumb += newNumb;
        } else {
            currentNumb += newNumb * Math.pow(0.1, posTen);
        }
        posTen++;
    }
    
    private void acceptInput(String input){
        result.setText(result.getText() + input);
        expandNumb(Integer.valueOf(input));
    }
    
    private void nextNumb(java.awt.event.ActionEvent evt, String symbol){
        prevInput.setText(String.valueOf(currentNumb));
        prevInput.setText(prevInput.getText() + symbol);
        operation = symbol;
        result.setText("");
        prevNumb = currentNumb;
        currentNumb = 0;
    }
    
